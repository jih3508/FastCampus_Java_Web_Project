package chapter08;

public class Sonata extends Car{
	
	@Override
	public void TurnOn() {
		System.out.println("Sonata 시동을 켭니다.");
	}

	@Override
	public void Drive() {
		System.out.println("Sonata 달립니다.");
	}

	@Override
	public void Break() {
		System.out.println("Sonata 멈춥니다.");
	}

	@Override
	public void TurnOff() {
		System.out.println("Sonata 시동을 끕니다.");
	}
}
